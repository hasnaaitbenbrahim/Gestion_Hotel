    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
            System.out.println("Attempting to delete Category with ID: " + id);

    int reponse = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer ce categorie?");
    if (reponse == 0) {
        Categorie categorieToDelete = Cat.findById(id);
        if (categorieToDelete == null) {
            JOptionPane.showMessageDialog(this, "Categorie non trouvée !");
            return;
        }

        // Try to delete the category
        boolean success = Cat.delete(categorieToDelete);
        if (success) {
            JOptionPane.showMessageDialog(this, "Categorie Bien Supprimé");
        } else {
            JOptionPane.showMessageDialog(this, "Erreur lors de la suppression de la catégorie");
        }
    }
    load();
    }//GEN-LAST:event_supprimerActionPerformed

