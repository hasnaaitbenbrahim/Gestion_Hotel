    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
       
        String Code = code.getText();
        String Libelle = libelle.getText();
    
        Categorie ct = Cat.findById(id);
        if (ct == null) {
            JOptionPane.showMessageDialog(this, "Categorie introuvable !");
            return; // Arrêter l'exécution si le client est introuvable
        }

        ct.setCode(Code);
        ct.setLibelle(Libelle);


         int reponse = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment modifier ce categorie ?");
        if (reponse == 0) {
            if (Cat.update(ct)) {
                JOptionPane.showMessageDialog(this, "Categorie Bien Modifié");
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de la modification du categorie");
            }
        }
        load();
    }//GEN-LAST:event_modifierActionPerformed

